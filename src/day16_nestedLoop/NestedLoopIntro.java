package day16_nestedLoop;

public class NestedLoopIntro {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println("Bismillah, her hayrin basidir");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println("Bismillah, her hayrin basidir");
        }


        for (int i = 0; i < 10; i++) {
            System.out.println("Bismillah, her hayrin basidir");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println("Bismillah, her hayrin basidir");
        }

        for (int i = 0; i < 10; i++) {
            System.out.println("Bismillah, her hayrin basidir");
        }

        System.out.println("----------------------------------------------------");

        for (int i = 0; i < 10; i++) { //i : 0

            for (int j = 0; j < 10; j++) {
                System.out.println("Bismillah, her hayrin basidir");
            }

        }




    }


}
